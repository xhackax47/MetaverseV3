local ESX = exports["es_extended"]:getSharedObject()

Citizen.CreateThread(function()
	while true do
		Citizen.Wait(1)
		local ped = PlayerPedId()
		local weapon = GetSelectedPedWeapon(ped)
		local stress = weaponstress[weapon] or 1000
		if IsPedShooting(ped) and weaponstress[weapon] > 0 then
		  TriggerEvent('metaverse_status:remove', 'stress', weaponstress[weapon])
		end
	end
end)

Citizen.CreateThread(function()
	while true do
		Citizen.Wait(100)
		local ped = PlayerPedId()
		if IsPedOnAnyBike(ped) then
		  TriggerEvent('metaverse_status:add','stress',3000)
		  Citizen.Wait(3000)
		end
	end
end)

Citizen.CreateThread(function()
	while true do
		Citizen.Wait(100)
		local ped = PlayerPedId()
		local veh = GetVehiclePedIsIn(ped)
		local speed = GetEntitySpeed(GetVehiclePedIsIn(ped,true)) * 2.6
		if IsPedInAnyVehicle(ped,false)	and speed >= 120 then
		  TriggerEvent('metaverse_status:remove','stress',25000)
		  Citizen.Wait(3000)
		end
	end
end)

